
public class ArithmeticDemo 
{
	public static void main(String args[])
	{
		int m=12,n=3;
		int r1=m+n;
		int r2=m-n;
		int r3=m*n;
		int r4=m/n;
		int r5=m%n;
		System.out.println(r1);
		System.out.println(r2);
		System.out.println(r3);
		System.out.println(r4);
		System.out.println(r5);
		n=n+m;
		System.out.println(n);
		n=++m;
		System.out.println(n);
		n=m++;
		System.out.println(n);
		
		
	}

}
